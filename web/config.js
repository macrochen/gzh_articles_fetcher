// Load settings from localStorage
export function loadSettings() {
  let presetPromptsStr = localStorage.getItem('gzh_preset_prompts');
  let presetPrompts = DEFAULT_PRESET_PROMPTS;
  if (presetPromptsStr) {
    try {
      presetPrompts = JSON.parse(presetPromptsStr);
    } catch(e) {
      // Fallback
    }
  }

  return {
    clientId: localStorage.getItem('gzh_web_client_id') || '',
    gemUrl: localStorage.getItem('gzh_gem_url') || '',
    apiKey: localStorage.getItem('gzh_api_key') || '',
    summaryPrompt: localStorage.getItem('gzh_summary_prompt') || defaultSummaryPrompt,
    presetPrompts: presetPrompts
  };
}

export function saveSettings(settings) {
  localStorage.setItem('gzh_web_client_id', settings.clientId);
  localStorage.setItem('gzh_gem_url', settings.gemUrl);
  localStorage.setItem('gzh_api_key', settings.apiKey);
  localStorage.setItem('gzh_summary_prompt', settings.summaryPrompt);
  localStorage.setItem('gzh_preset_prompts', JSON.stringify(settings.presetPrompts));
}
